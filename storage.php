          <h2>Data Storage and Retrieval</h2>
          <div class="clr"></div>
          <p><img src="images/img_1.jpg" width="613" height="193" alt="" /> </p>
          <div class="clr"></div>
          
          <p>Visibility data and header information are stored on files in a format which is a simple extension of the one used by the single dish CLASS program. The file organisation is exactly the same as in CLASS; except for some additional index entries, specific to interferometric data. Observation headers are also organised in sections, most of which are interferometer-specific. However, the length of the data section may vary, depending on the number of data records written sequentially. Each data record contains a short data header with the values of some fast varying parameters such as time, U and V coordinates, source direction vector, ...
<br>
The Input and Output files are selected by the FILE command (syntax: as in CLASS).<br>

Most commands deal with all observations in the Current Index. The Current Index is filled by means of the command FIND. Selection criteria are mostly identical to the ones in CLASS, with some additions such as Hour Angle or Calibration Status. Commands DROP and IGNORE have the same meaning as in CLASS. Commands such as GET and WRITE are provided for test purposes only.<br>
Depending on whether the input and output files are identical or not commands which output data will either modify data in place, or write new versions of the data observations. Command COMPRESS, is normally used to time-compress data. To simply transfer data from one file to another, the command COPY should be preferred.
<br>
Finally command TABLE outputs a UV Data Table suitable for use in mapping programs (either GILDAS tasks, or AIPS tasks after format conversion by GFITS).
</p>

