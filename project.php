          <h2>project management</h2>
          <div class="clr"></div>
          <p><img src="images/img_1.jpg" width="613" height="193" alt="" /> </p>
          <div class="clr"></div>
          
          <p>A project is a unique, transient endeavour, undertaken to achieve planned objectives, which could be defined in terms of outputs, outcomes or benefits. A project is usually deemed to be a success if it achieves the objectives according to their acceptance criteria, within an agreed timescale and budget.
<br>
A key factor that distinguishes project management from just 'management' is that it has this final deliverable and a finite timespan, unlike management which is an ongoing process. Because of this a project professional needs a wide range of skills; often technical skills, and certainly people management skills and good business awareness.Written for anyone new to projects or wishing to progress their career as a project professional Starting Out charts the journey of the APM project life cycle, from concept through to delivery and handover.
<br>
Along the way readers will learn about the fundamental features of project management, including ownership of the business case, engaging with stakeholders and realising the all-important benefits of the project, plus much, much more.
</p>
<h3>When do you use project management?</h3>
<p>Projects are separate from business-as-usual activities, requiring people to come together temporarily to focus on specific project objectives. As a result, effective teamwork is central to successful projects. 
<br>
Project management is concerned with managing discrete packages of work to achieve specific objectives. The way the work is managed depends upon a wide variety of factors.
<br>
The scale, significance and complexity of the work are obvious factors: relocating a small office and organising the Olympics share many basic principles, but offer very different managerial challenges.</p>
<h3>Why do we use project management?</h3>
<p>Project management is essentially aimed at producing an end product that will effect some change for the benefit of the organisation that instigated the project. It is the initiation, planning and control of a range of tasks required to deliver this end product.</p>
